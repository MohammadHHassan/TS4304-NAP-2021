package day1;

public class LearningAssignmentOperator
{
	public static void main(String[] args)
	{
		int x=1;
		System.out.println("Initially, x = " + x);
		
		x = x+1;
		System.out.println("Incremented, x = " + x);
		
		x++;
		System.out.println("Incremented, x = " + x);
		
		x += 1;
		System.out.println("Incremented, x = " + x);
		
		x--;
		System.out.println("Decremented, x = " + x);
		
		x -= 1;
		System.out.println("Decremented, x = " + x);
		
		x = x-1;
		System.out.println("Decremented, x = " + x);
	}
}
