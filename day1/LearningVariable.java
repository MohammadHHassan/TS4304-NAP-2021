package day1;

public class LearningVariable
{
	public static void main(String[] args)
	{
		int a = 30;
		double b = 3.14;
		char c = 'M';
		boolean d = true;
		String e = "Mohammad";
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
		System.out.println("d = " + d);
		System.out.println("e = " + e);
	}
}